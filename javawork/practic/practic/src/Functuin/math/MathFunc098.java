package Functuin.math;

public class MathFunc098 {
    public static int add(int re2,int re3){
        return re2+re3;
    }
    public static double subtract(int aa1,int aa2){
        return aa1-aa2;
    }
    public static double multipy(int aa1,int aa2){
        return aa1*aa2;
    }
    public static double divide(int aa1,int aa2){
        return aa1/aa2;
    }

    public static Boolean checkInteger(int aa){
        if(aa < 0)return false;
        return true;
    }
}
